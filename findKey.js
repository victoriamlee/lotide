const findKey = function(object, callback) {
  const arrOne = Object.keys(object);
  const arrTwo = Object.values(object);
  for (let i = 0; i < arrTwo.length; i++) {
    if (callback(arrTwo[i])) {
      return arrOne[i];
    }
  }
};
  
